import keyboard

# 1. Регистрация горячих клавиш и обработчиков
# Простая горячая клавиша:
keyboard.add_hotkey('ctrl+a', lambda: print('Горячая клавиша сработала'))

# 2. Проверка состояния клавиш
# Проверить, зажата ли клавиша:
if keyboard.is_pressed('space'):
    print('Пробел нажат!')

# 3. Эмуляция нажатия клавиш.
# Ввести текст программно:
keyboard.write('Привет!')

# Нажать и отпустить клавишу:
keyboard.press_and_release('enter')

# 4. Запись и воспроизведение
# Запись всех нажатий (до нажатия ESC):
print('Записываю нажатия, жми ESC для остановки...')
events = keyboard.record(until='esc')

# Воспроизведение записанных действий:
print('Воспроизвожу записанное...')
keyboard.play(events)

# 5. Снятие горячих клавиш.
# Удалить определённую горячую клавишу:
keyboard.remove_hotkey('ctrl+a')

# Удалить все горячие клавиши:
keyboard.unhook_all()

# 6. Перехват и блокировка.
# Перехватить и заблокировать нажатие клавиши 'q':
keyboard.block_key('q')

print('Программа работает... Нажми ESC для выхода.')
keyboard.wait('esc')
